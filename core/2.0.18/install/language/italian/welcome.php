<u><b>Cos'è XOOPS?</b></u>
<p>
<b>XOOPS</b> è uno script 'Object Oriented' open source scritto in PHP.</b><br />
E' lo strumento ideale per sviluppare piccoli e grandi siti web, portali intranet per aziende e molto altro.
</p>
<p>
XOOPS viene rilasciato secondo i termini della <a href='http://www.gnu.org/copyleft/gpl.html' target='_blank'>GNU General Public License (GPL)</a>.</b><br />
Inoltre può essere liberamente distribuito tenendo fede ai termini di distribuzione descritti dalla stessa GPL.
</p>
<u><b>Requisiti</b></u>
<p>
<ul>
<li> <a href='http://www.apache.org/' target='_blank'>Apache</a> Web Server o qualsiasi altro server che supporti la versione PHP richiesta (IIS, Roxen, ecc.).</li>
<li><a href='http://www.php.net/' target='_blank'>PHP</a> 4.3 o successivo (PHP 4.2.x può funzionare ma non è ufficialmente supportato)</li>
<li><a href='http://www.mysql.com/' target='_blank'>MySQL</a> Database 3.23.X o successivo</li>
<li><a href='http://www.phpmyadmin.net/' target='_blank'>phpMyAdmin</a> 2.8.0.2 Per gestire facilmente MySQL (opzionale)</li>
</ul>
</p>
<u><b>Prima di iniziare l'installazione</b></u>
<br /><br />
Se desideri installare XOOPS in <font color='brown'><b>Locale</b></font> 
<ul>
<li>Inserisci l'account \"root\" senza password, dovrebbe funzionare in tutti i database MySQL.</li>
<li>Sotto windows non preoccuparti di rendere scrivibili le cartelle uploads/, cache/ e templates_c/ e il file mainfile.php.</li>
<li>Abilita i cookies e i Javascript nel tuo browser.</li>
<li><b>Importante:</b> Se hai un firewall installato, controlla che non blocchi i dati inviati attraverso il tuo browser, altrimenti non ti sarà possibile accedere loggarti al termine dell'installazione, ed accedere così al pannello di amministrazione di XOOPS.</li>
</ul>
<br />
Se desideri installare XOOPS in <font color='brown'><b>Remoto</b></font> 
<ul>
<li>Crea un database per il tuo sito o, se non puoi farlo, perchè non hai i diritti di accesso necessari, richiedi <b>all'amministratore del tuo spazio web</b> di creare un nuovo database.</li>
<li>Crea un account utente e garantisci all'utente i diritti di accesso al database o, se non puoi farlo, perchè non hai i diritti di accesso necessari, richiedi
all'amministratore del tuo spazio web di creare un nuovo account che abbia i diritti di accesso al database, creato in precedenza.</li>
<li>Rendi scrivibili (CHMOD 777) le cartelle <b>uploads/</b>, <b>cache/</b> e <b>templates_c/</b> e il file <b>mainfile.php</b>. Se non puoi farlo, chiedi <b>all'amministratore del tuo spazio web</b> di indicarti una cartella che abbia i permessi abilitati sia in lettura che in scrittura. Sposta quindi tutti i file di XOOPS in questa cartella.</li>
<li>Abilita i cookies e i Javascript nel tuo browser.</li>
<li>Infine, se hai un firewall installato, controlla che non blocchi i dati inviati attraverso il tuo browser, altrimenti non ti sarà possibile accedere loggarti al termine dell'installazione, ed accedere così al pannello di amministrazione di XOOPS.</li>
</ul>
